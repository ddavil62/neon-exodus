// ── 현재 언어 ──

let currentLocale = 'en';

/**
 * 현재 언어를 반환한다.
 * @returns {string} 'ko' 또는 'en'
 */
export function getLocale() {
  return currentLocale;
}

/**
 * 언어를 설정한다.
 * @param {string} locale - 'ko' 또는 'en'
 */
export function setLocale(locale) {
  if (TRANSLATIONS[locale]) {
    currentLocale = locale;
  }
}

/**
 * 언어를 토글한다 (ko <-> en).
 * @returns {string} 전환 후 언어
 */
export function toggleLocale() {
  currentLocale = currentLocale === 'ko' ? 'en' : 'ko';
  return currentLocale;
}

/**
 * 번역된 텍스트를 반환한다.
 * @param {string} key - 번역 키
 * @param {...*} args - 플레이스홀더 대체값 ({0}, {1}, ...)
 * @returns {string} 번역된 문자열
 */
export function t(key, ...args) {
  const dict = TRANSLATIONS[currentLocale] || TRANSLATIONS['ko'];
  let text = dict[key] || key;

  // 플레이스홀더 치환
  args.forEach((val, i) => {
    text = text.replace(`{${i}}`, val);
  });

  return text;
}
